    private void rechActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rechActionPerformed
        Object[] object=new Object[]{"CNE","name","lastname"};
        String s = (String) JOptionPane.showInputDialog(rech, 
        "Rechercher par ?",
        "Type de recherche",
        JOptionPane.QUESTION_MESSAGE, 
        null, 
        object, 
        object[0]);
        int x;
        String valrechercher=txt.getText();
        ArrayList<Object[]> resultat=new ArrayList<Object[]>();
        
        if(s.equals("CNE")){
            x=dtm.getRowCount();
           
           
             

        
            for(int i=0;i<x;i++){
                String check=(String) dtm.getValueAt(i, 0);
                
                if(check.equals(valrechercher)){
                    
                    resultat.add(new Object[]{dtm.getValueAt(i, 0),dtm.getValueAt(i, 1),dtm.getValueAt(i, 2),dtm.getValueAt(i, 3),dtm.getValueAt(i,4),dtm.getValueAt(i, 5)});
                      
                }
            }
            dtm.setNumRows(0);
            for(Object[] o:resultat ){
                dtm.addRow(o);
            }
            
        }
        if(s.equals("name")){
             x=dtm.getRowCount();
           
           
             

        
            for(int i=0;i<x;i++){
                String check=(String) dtm.getValueAt(i, 1);
                
                if(check.equals(valrechercher)){
                    
                    resultat.add(new Object[]{dtm.getValueAt(i, 0),dtm.getValueAt(i, 1),dtm.getValueAt(i, 2),dtm.getValueAt(i, 3),dtm.getValueAt(i,4),dtm.getValueAt(i, 5)});
                      
                }
            }
            dtm.setNumRows(0);
            for(Object[] o:resultat ){
                dtm.addRow(o);
            }
            
        }
        if(s.equals("lastname")){
             x=dtm.getRowCount();
           
           
             

        
            for(int i=0;i<x;i++){
                String check=(String) dtm.getValueAt(i, 2);
                
                if(check.equals(valrechercher)){
                    
                    resultat.add(new Object[]{dtm.getValueAt(i, 0),dtm.getValueAt(i, 1),dtm.getValueAt(i, 2),dtm.getValueAt(i, 3),dtm.getValueAt(i,4),dtm.getValueAt(i, 5)});
                      
                }
            }
            dtm.setNumRows(0);
            for(Object[] o:resultat ){
                dtm.addRow(o);
            }
            
        }
        
        
        
    }//GEN-LAST:event_rechActionPerformed

