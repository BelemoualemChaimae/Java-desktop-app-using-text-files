    private void mqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mqlActionPerformed
        dtm=(DefaultTableModel)tab.getModel();
       
        try {
                E=new ArrayList<Etudiant>();
                tf=new traitementFichier("Mql.txt");
                E=tf.Lecture(E);
                dtm.setNumRows(0);
                for(Etudiant e:E){
                    dtm.addRow(new Object[]{e.getCNE(),e.getName(),e.getLastname(),e.getDaten(),e.getFil(),e.getSexe()});
                }
            
            
        } catch (IOException ex) {
            Logger.getLogger(Chercher.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_mqlActionPerformed

