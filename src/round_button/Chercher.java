    public ArrayList<Etudiant> rechercher(ArrayList<Etudiant> E,String s,String rech){
          ArrayList<Etudiant> taBreturner=new ArrayList<Etudiant>();
          String nv;
          for(Etudiant e:E){
              nv="e.get"+rech+"()";
              if(nv.equals(s)){
                  taBreturner.add(e);
              }
          }
          return taBreturner;
    }
